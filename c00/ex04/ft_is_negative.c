#include <unistd.h>

void	ft_is_negative(int n)
{
	int	N;
	int	P;

	N = 'N';
	P = 'P';
	if (n >= 0)
	{
		write(1, &P, 4);
	}
	else
	{
		write(1, &N, 4);
	}
}
