int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 1)
		return (1);
	i = 0;
	while (i * i <= nb && i < 46341)
	{
		if (i * i == nb)
			return (i);
		++i;
	}
	return (0);
}
